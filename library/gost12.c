/*
 * output = GOST12( input buffer )
 */
void mbedtls_gost12( const unsigned char *input, size_t ilen,
             unsigned char output[64], int is256 )
{
    mbedtls_gost12_context ctx;

    mbedtls_gost12_init( &ctx );
    mbedtls_gost12_starts( &ctx, is256 );
    mbedtls_gost12_update( &ctx, input, ilen );
    mbedtls_gost12_finish( &ctx, output );
    mbedtls_gost12_free( &ctx );
}

