#define X(k,a,i)                 \
{                                \
    for( i = 0; i < 64; i++ )    \
        k[i] ^= a[i];            \
}

#define PS(a,c,i,j)                                             \
{                                                               \
    for( i = 0; i < 8; i++ )                                    \
    {                                                           \
        a[ ( i << 3 ) + i ] = Pi[ a[ ( i << 3 ) + i ] ];        \
        for( j = i + 1; j < 8; j++ )                            \
        {                                                       \
            c = a[ ( i << 3 ) + j ];                            \
            a[ ( i << 3 ) + j ] = Pi[ a[ i + ( j << 3 ) ] ];    \
            a[ i + ( j << 3 ) ] = Pi[ c ];                      \
        }                                                       \
    }                                                           \
}

#define l(res,a,i)              \
{                               \
    res = 0;                    \
    i = 63;                     \
    while( a != 0 )             \
    {                           \
        if( ( a & 1 ) == 1 )    \
            res ^= A[i];        \
        a >>= 1;                \
        i--;                    \
    }                           \
}

#define L(a,tmp1,tmp2,i)                                                               \
{                                                                                      \
    GET_UINT64_LE( tmp1, a, 0  ); l( tmp2, tmp1, i ); PUT_UINT64_LE( tmp2, a, 0  );    \
    GET_UINT64_LE( tmp1, a, 8  ); l( tmp2, tmp1, i ); PUT_UINT64_LE( tmp2, a, 8  );    \
    GET_UINT64_LE( tmp1, a, 16 ); l( tmp2, tmp1, i ); PUT_UINT64_LE( tmp2, a, 16 );    \
    GET_UINT64_LE( tmp1, a, 24 ); l( tmp2, tmp1, i ); PUT_UINT64_LE( tmp2, a, 24 );    \
    GET_UINT64_LE( tmp1, a, 32 ); l( tmp2, tmp1, i ); PUT_UINT64_LE( tmp2, a, 32 );    \
    GET_UINT64_LE( tmp1, a, 40 ); l( tmp2, tmp1, i ); PUT_UINT64_LE( tmp2, a, 40 );    \
    GET_UINT64_LE( tmp1, a, 48 ); l( tmp2, tmp1, i ); PUT_UINT64_LE( tmp2, a, 48 );    \
    GET_UINT64_LE( tmp1, a, 56 ); l( tmp2, tmp1, i ); PUT_UINT64_LE( tmp2, a, 56 );    \
}

#define LPS(a,c,tmp1,tmp2,i,j)    \
{                                 \
    PS( a, c, i, j );             \
    L( a, tmp1, tmp2, i );        \
}

void mbedtls_gost12_process( mbedtls_gost12_context *ctx, const unsigned char data[64] )
{
    int i, j, k;
    uint64_t tmp1, tmp2;
    unsigned char K[64], res[64], c;

    memcpy( K, ctx->h, 64 );
    memcpy( res, data, 64 );

    PUT_UINT64_LE( ( ctx->N << 3 ), K, 0 );

    /*
     * K = LPS(h ^ N)
     */
    for( i = 0; i < 8; i++ )
    {
        K[i] ^= ctx->h[i];
    }
    LPS( K, c, tmp1, tmp2, i, j );

    /*
     * 13 rounds of encryption
     */
    X( res, K, i );
    for( k = 0; k < 12; k++ )
    {
        LPS( res, c, tmp1, tmp2, i, j );

        X( K, C[k], i );
        LPS( K, c, tmp1, tmp2, i, j );

        X( res, K, i );
    }

    /*
     * h = res ^ h ^ m
     */
    X( ctx->h, res, i );
    X( ctx->h, data, i );

    /*
     * Update N
     */
    ctx->N += 64;

    /*
     * Update control sum
     */
    c = 0;
    for( i = 0; i < 64; i++ )
    {
        ctx->sum[i] += c;
        c = (unsigned char) ( ctx->sum[i] < c );
        ctx->sum[i] += data[i];
        c += (unsigned char) ( ctx->sum[i] < data[i] );
    }
}
