/*
 * GOST12 final digest
 */
void mbedtls_gost12_finish( mbedtls_gost12_context *ctx, unsigned char output[64] )
{
    size_t last, padn;
    uint64_t high, low;
    unsigned char msglen[64], sum[64];

    memset( msglen, 0, 64 );

    high = ( ctx->total[0] >> 61 )
         | ( ctx->total[1] <<  3 );
    low  = ( ctx->total[0] <<  3 );

    PUT_UINT64_LE( low,  msglen, 0 );
    PUT_UINT64_LE( high, msglen, 8 );

    last = (size_t)( ctx->total[0] & 0x3F );
    padn = ( last != 0 ) ? ( 64 - last ) : ( 0 );

    mbedtls_gost12_update( ctx, gost12_padding, padn );
    memcpy( sum, ctx->sum, 64 );
    ctx->N = 0;
    mbedtls_gost12_process( ctx, msglen );
    ctx->N = 0;
    mbedtls_gost12_process( ctx, sum );

    if( ctx->is256 == 0 )
        memcpy( output, ctx->h, 64 );
    else
        memcpy( output, &ctx->h[32], 32 );
}

