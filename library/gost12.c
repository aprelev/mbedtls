/* Implementation that should never be optimized out by the compiler */
static void mbedtls_zeroize( void *v, size_t n ) {
    volatile unsigned char *p = (unsigned char*)v; while( n-- ) *p++ = 0;
}

/*
 * 64-bit integer manipulation macros (little endian)
 */
#ifndef GET_UINT64_LE
#define GET_UINT64_LE(n,b,i)                         \
{                                                    \
    (n) = ( (uint64_t) (b)[(i)    ]       )          \
        | ( (uint64_t) (b)[(i) + 1] <<  8 )          \
        | ( (uint64_t) (b)[(i) + 2] << 16 )          \
        | ( (uint64_t) (b)[(i) + 3] << 24 )          \
        | ( (uint64_t) (b)[(i) + 4] << 32 )          \
        | ( (uint64_t) (b)[(i) + 5] << 40 )          \
        | ( (uint64_t) (b)[(i) + 6] << 48 )          \
        | ( (uint64_t) (b)[(i) + 7] << 56 );         \
}
#endif

#ifndef PUT_UINT64_LE
#define PUT_UINT64_LE(n,b,i)                         \
{                                                    \
    (b)[(i)    ] = (unsigned char) ( (n)       );    \
    (b)[(i) + 1] = (unsigned char) ( (n) >>  8 );    \
    (b)[(i) + 2] = (unsigned char) ( (n) >> 16 );    \
    (b)[(i) + 3] = (unsigned char) ( (n) >> 24 );    \
    (b)[(i) + 4] = (unsigned char) ( (n) >> 32 );    \
    (b)[(i) + 5] = (unsigned char) ( (n) >> 40 );    \
    (b)[(i) + 6] = (unsigned char) ( (n) >> 48 );    \
    (b)[(i) + 7] = (unsigned char) ( (n) >> 56 );    \
}
#endif

void mbedtls_gost12_init( mbedtls_gost12_context *ctx )
{
    memset( ctx, 0, sizeof( mbedtls_gost12_context ) );
}

void mbedtls_gost12_free( mbedtls_gost12_context *ctx )
{
    if( ctx == NULL )
        return;

    mbedtls_zeroize( ctx, sizeof( mbedtls_gost12_context ) );
}

void mbedtls_gost12_clone( mbedtls_gost12_context *dst,
                           const mbedtls_gost12_context *src )
{
    *dst = *src;
}

/*
 * GOST12 context setup
 */
void mbedtls_gost12_starts( mbedtls_gost12_context *ctx, int is256 )
{
    int i;

    ctx->total[0] = 0;
    ctx->total[1] = 0;

    ctx->N = 0;

    for( i = 0; i < 64; i++ )
    {
        if( is256 == 0 )
            ctx->h[i] = 0;
        else
            ctx->h[i] = 1;

        ctx->sum[i] = 0;
    }

    ctx->is256 = is256;
}

